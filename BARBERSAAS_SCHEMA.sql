CREATE INDEX idx_barber_specialties_specialty_id ON barber_specialties(specialty_id);

CREATE INDEX idx_services_service_type_id ON services(service_type_id);
CREATE INDEX idx_service_barbers_service_id ON service_barbers(service_id);
CREATE INDEX idx_service_barbers_barber_id ON service_barbers(barber_id);

CREATE INDEX idx_appointments_barber_id ON appointments(barber_id);
CREATE INDEX idx_appointments_client_id ON appointments(client_id);
CREATE INDEX idx_appointments_status_id ON appointments(status_id);

CREATE INDEX idx_appointments_services_appointment_id ON appointments_services(appointment_id);
CREATE INDEX idx_appointments_services_service_id ON appointments_services(service_id);

CREATE INDEX idx_gallery_category_id ON gallery(category_id);
CREATE INDEX idx_gallery_barber_id ON gallery(barber_id);

CREATE INDEX idx_financial_transactions_type_id ON financial_transactions(type_id);
CREATE INDEX idx_financial_transactions_category_id ON financial_transactions(category_id);
CREATE INDEX idx_financial_transactions_appointment_id ON financial_transactions(appointment_id);
